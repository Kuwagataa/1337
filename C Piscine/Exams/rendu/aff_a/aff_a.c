#include <unistd.h>

int	main(int argc, char **argv)
{
	int	x;

	x = 0;
	if (argc == 2)
	{
		while (argv[1][x] != '\0')
		{
			if (argv[1][x] == 'a')
			{
				write(1, "a", 1);
				break ;
			}
			x++;
		}
	}
	else
		write(1, "a", 1);
	write(1, "\n", 1);
}
