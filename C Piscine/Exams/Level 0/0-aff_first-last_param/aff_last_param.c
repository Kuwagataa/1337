#include<unistd.h>

int	main(int argc, char **argv)
{
	int	x;

	x = 0;
	if (argc == 1)
	{
		write(1, "\n", 1);
	}
	else if (argc >= 2)
	{
		while (argv[argc - 1][x] != '\0')
		{
			write(1, &argv[argc - 1][x++], 1);
		}
		write(1, "\n", 1);
	}
}
